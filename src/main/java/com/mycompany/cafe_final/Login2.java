    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String username = jTextField1.getText();
String password = String.valueOf(jPasswordField1.getPassword());

try {
    String url = "jdbc:derby://localhost:1527/Cafe";
    Connection con = DriverManager.getConnection(url, "admin", "1234");
    String qry = "SELECT * FROM APP.USERS_INFO WHERE Username = ? AND Password = ?";
    PreparedStatement pst = con.prepareStatement(qry);
    pst.setString(1, username);
    pst.setString(2, password);

    ResultSet rs = pst.executeQuery();
    
    boolean flag = false;
    while (rs.next()) {
        String u = rs.getString("Username");
        String p = rs.getString("Password");
        
        if (username.equalsIgnoreCase(u) && password.equals(p)) {
            flag = true;
            JOptionPane.showMessageDialog(this, "Accesso riuscito!", "Greeting", JOptionPane.INFORMATION_MESSAGE);
            
            
            Customer2 Customer2Frame = new Customer2();
            Customer2Frame.setVisible(true);
            Customer2Frame.pack();
            Customer2Frame.setLocationRelativeTo(null);
            this.dispose(); 
            break;
        }
    }

    if (!flag) {
        JOptionPane.showMessageDialog(this, "Nome utente o password errati. Riprova!", "Error", JOptionPane.ERROR_MESSAGE);
    }

} catch (Exception e) {
    System.out.println(e.getMessage());
}

    }//GEN-LAST:event_jButton1ActionPerformed

