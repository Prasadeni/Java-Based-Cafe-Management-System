    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        
    String username = jTextField1.getText();
    String newPassword = String.valueOf(jPasswordField1.getPassword());
    

    int confirmation = JOptionPane.showConfirmDialog(this, "Sei sicuro di voler aggiornare le informazioni dell'utente?", "Confirm Update", JOptionPane.YES_NO_OPTION);
    if (confirmation == JOptionPane.YES_OPTION) {
        try {
            String url = "jdbc:derby://localhost:1527/Cafe";
            Connection con = DriverManager.getConnection(url, "admin", "1234");
            String qry = "UPDATE APP.USERS_INFO SET Password = ? WHERE Username = ?";
            PreparedStatement pst = con.prepareStatement(qry);
            pst.setString(1, newPassword);
            pst.setString(2, username);

            int rowsAffected = pst.executeUpdate();
            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(this, "Password utente aggiornata con successo!", "Success", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "Utente non trovato o aggiornamento non riuscito.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Errore durante l'aggiornamento delle informazioni sull'utente.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }


    }//GEN-LAST:event_jButton3ActionPerformed

